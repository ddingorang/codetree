import java.util.Scanner;

public class Main {

    static int recur(int n) {
        if(n==0) {
            return 1;
        }
        else if(n==1) {
            return 1;
        }
        return n*recur(n-1);
    }
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        // Please write your code here.
        System.out.println(recur(n));
    }
}